-- Xử lý ký tự % hoặc _ trong chuỗi bằng ESCAPE(\)
SELECT *
FROM EMPLOYEE
WHERE JOB_POSITION LIKE '%B_';

SELECT *
FROM EMPLOYEE
WHERE JOB_POSITION LIKE '%!%B_' ESCAPE '!';

-- Cont 2 
-- is null
SELECT *
FROM EMPLOYEE
WHERE MANAGER IS NULL;

-- Chỉ dùng is null không so sánh với null vì so sánh với null thì luôn null.

-- And, or, not
SELECT *
FROM EMPLOYEE
WHERE SALARY NOT IN(500000);

-- Cont 3 order by
SELECT *
FROM EMPLOYEE
ORDER BY SALARY DESC;













